package datastructureandalgorithm1.sort;

public class LearningSwap {
    public static void main(String[] args) {
        int num1=40;
        int num2=30;
        int temp;
        System.out.println("Number1 value is "+num1);
        System.out.println("Number2 vaule is "+num2);

        temp=num1;//40
        num2=temp;//40
        num1=temp+num2;//40+40
        temp=num1;//80
        System.out.println(temp);
    }














}
